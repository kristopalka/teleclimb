/***
 * Access class to color palette of the application. Methods return hex representation of 5 different colors.
 */
class ColorProbe {
  getRedCrayola() {
    return "#ed254e";
  }

  getNaplesYellow() {
    return "#f9dc5c";
  }

  getMintCream() {
    return "#f4fffd";
  }

  getOxfordBlue() {
    return "#011936";
  }

  getBlackCoral() {
    return "#465362";
  }

  getNHGrey() {
    return "#383834";
  }
}
